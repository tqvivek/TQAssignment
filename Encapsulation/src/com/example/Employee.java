package com.example;

public class Employee {

	private int id;
	private String name;
	private double salary;

	public void setEmpid(int id) {
		this.id = id;
	}

	public int getEmpid() {
		return id;
	}

	public void setEmpName(String name) {
		this.name = name;
	}

	public String getEmpName() {
		return name;
	}

	public void setEmpSalary(double salary) {
		this.salary = salary;
	}

	public double getEmpSalary() {
		return salary;
	}

}
